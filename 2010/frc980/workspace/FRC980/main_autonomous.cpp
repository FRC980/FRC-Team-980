void Main::AutonomousPeriodic(void)
{
    //--- Get the Robot instance
    //Robot980* pRobot = Robot980::GetInstance();

    //--- Feed the watchdog
    GetWatchdog().Feed();
    
    //--- Switch to the correct autonomous mode
    switch(iMode)
    {
       default:
       case 1:
    	   this->Auton1();
    	   break;

       case 2:
    	   this->Auton2();
    	   break;

       case 3:
    	   this->Auton3();
    	   break;

       case 4:
    	   this->Auton4();
    	   break;

       case 5:
    	   this->Auton5();
    	   break;

       case 6:
    	   this->Auton6();
    	   break;
    	   
    }
}

